export function getTimezonesByRegion() {
  const regions: Record<string, Timezone[]> = {}
  
  TIMEZONES.forEach(timezone => {
    if (!regions[timezone.region]) {
      regions[timezone.region] = []
    }
    regions[timezone.region].push(timezone)
  })
  
  // Sort timezones within each region
  Object.keys(regions).forEach(region => {
    regions[region].sort((a, b) => a.label.localeCompare(b.label))
  })
  
  return regions
}

export function findTimezone(value: string): Timezone | undefined {
  return TIMEZONES.find(tz => tz.value === value)
}

